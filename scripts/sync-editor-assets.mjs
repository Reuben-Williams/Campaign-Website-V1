import { copyFileSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, dirname, extname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif", ".svg", ".avif"]);
const copiedSourceFolders = ["asw_carmenmorales", "morales4assembly"];
const publicImageFolders = ["public/images/campaign"];
const generatedDir = join(projectRoot, "src/generated");
const builderMediaRoot = join(projectRoot, "public/builder-media");

function walkFiles(directory) {
  const entries = readdirSync(directory, { withFileTypes: true });
  return entries.flatMap((entry) => {
    const fullPath = join(directory, entry.name);
    if (entry.isDirectory()) return walkFiles(fullPath);
    return [fullPath];
  });
}

function isImage(filePath) {
  return imageExtensions.has(extname(filePath).toLowerCase());
}

function toPublicPath(filePath) {
  return `/${relative(join(projectRoot, "public"), filePath).split(sep).join("/")}`;
}

function toLabel(filePath) {
  return basename(filePath, extname(filePath))
    .replace(/[_-]+/g, " ")
    .replace(/\b\w/g, (character) => character.toUpperCase());
}

function toId(publicPath) {
  return publicPath
    .replace(/^\//, "")
    .replace(/\.[^.]+$/, "")
    .replace(/[^a-zA-Z0-9]+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
}

function mimeTypeFor(filePath) {
  const extension = extname(filePath).toLowerCase();
  if (extension === ".png") return "image/png";
  if (extension === ".webp") return "image/webp";
  if (extension === ".gif") return "image/gif";
  if (extension === ".svg") return "image/svg+xml";
  if (extension === ".avif") return "image/avif";
  return "image/jpeg";
}

function copyRootFolderImages() {
  const copiedFiles = [];
  for (const folder of copiedSourceFolders) {
    const sourceRoot = join(projectRoot, folder);
    const files = walkFiles(sourceRoot).filter(isImage);
    for (const file of files) {
      const relativePath = relative(sourceRoot, file);
      const destination = join(builderMediaRoot, folder, relativePath);
      mkdirSync(dirname(destination), { recursive: true });
      copyFileSync(file, destination);
      copiedFiles.push(destination);
    }
  }
  return copiedFiles;
}

function discoverPublicImages(copiedFiles) {
  const discovered = [...copiedFiles];
  for (const folder of publicImageFolders) {
    const absoluteFolder = join(projectRoot, folder);
    discovered.push(...walkFiles(absoluteFolder).filter(isImage));
  }
  return discovered.sort((left, right) => toPublicPath(left).localeCompare(toPublicPath(right)));
}

function readSitePageBlocks() {
  const source = readFileSync(join(projectRoot, "src/content/site.ts"), "utf8");
  const slugMatches = [...source.matchAll(/\n  \{\s*\n    slug: "([^"]+)"/g)];
  return slugMatches.map((match, index) => {
    const start = match.index ?? 0;
    const end = slugMatches[index + 1]?.index ?? source.indexOf("\n];", start);
    return source.slice(start, end);
  });
}

function readStringField(block, field) {
  return block.match(new RegExp(`${field}: "([^"]+)"`))?.[1] ?? "";
}

function readMultilineField(block, field) {
  return block.match(new RegExp(`${field}:\\s*\\n\\s*"([^"]+)"`))?.[1] ?? readStringField(block, field);
}

function generatePosts() {
  return readSitePageBlocks()
    .map((block) => {
      const slug = readStringField(block, "slug");
      const href = readStringField(block, "href") || `/${slug}`;
      const title = readStringField(block, "title");
      const summary = readMultilineField(block, "summary");
      const imagePath = readStringField(block, "src");
      const sectionTitle = readStringField(block, "title") || title;
      return {
        id: `placeholder-${slug}`,
        title: title || sectionTitle || `${slug} post placeholder`,
        slug: `${slug}-placeholder`,
        status: "draft",
        excerpt: summary || "Placeholder post generated from existing site content.",
        imagePath,
        source: "site-scan",
        sourcePagePath: href,
        linkedRegionIds: [`${slug}.hero.title`, `${slug}.hero.summary`, `${slug}.hero.image`],
      };
    })
    .filter((post) => post.id !== "placeholder-home");
}

function renderTs(name, value) {
  const formatted = JSON.stringify(value, null, 2).replace(/^(\s*)"([a-zA-Z_$][\w$]*)":/gm, "$1$2:");
  return `// Generated by scripts/sync-editor-assets.mjs. Do not edit by hand.\n\nexport const ${name} = ${formatted} as const;\n`;
}

mkdirSync(generatedDir, { recursive: true });
const copiedFiles = copyRootFolderImages();
const mediaAssets = discoverPublicImages(copiedFiles).map((filePath) => {
  const publicPath = toPublicPath(filePath);
  return {
    id: toId(publicPath),
    siteId: "campaign-website-v1",
    path: publicPath,
    alt: toLabel(filePath),
    label: toLabel(filePath),
    mimeType: mimeTypeFor(filePath),
    source: "seed",
    userId: "project-scan",
    createdAt: "1970-01-01T00:00:00.000Z",
  };
});

writeFileSync(join(generatedDir, "editor-media-assets.ts"), renderTs("editorMediaAssets", mediaAssets));
writeFileSync(join(generatedDir, "editor-posts.ts"), renderTs("editorPostPlaceholders", generatePosts()));

console.log(`Copied ${copiedFiles.length} root images and generated ${mediaAssets.length} media records.`);
